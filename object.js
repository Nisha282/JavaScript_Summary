// object 

/* Object is a collection of key and their values .


*/

const fruit={
    name : "Apple",
    quantity : "1",
    customer : "only me"
}
console.log(fruit)

console.log(Object.keys(fruit)) //return only key of objects as array

console.log(Object.values(fruit)) //return only values of objects as array

console.log(Object.entries(fruit)) //return both key and value of objects as array

